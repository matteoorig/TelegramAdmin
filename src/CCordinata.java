public class CCordinata {
    float lat = 0;
    float lon = 0;
    String image = "";


    public CCordinata(){}

    public void setLat(float l){
        this.lat = l;
    }
    public void setLon(float l){
        this.lon = l;
    }

    public float getLat() {
        return lat;
    }

    public float getLon() {
        return lon;
    }
}
