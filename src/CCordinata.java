public class CCordinata {
    double lat = 0;
    double lon = 0;
    String image = "";


    public CCordinata(){}

    public void setLat(double l){
        this.lat = l;
    }
    public void setLon(double l){
        this.lon = l;
    }

    public double getLat() {
        return lat;
    }

    public double getLon() {
        return lon;
    }
}
