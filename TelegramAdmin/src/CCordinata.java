public class CCordinata {
    float lat;
    float lon;

    public CCordinata(){}

    public void setLat(float l){
        this.lat = l;
    }
    public void setLon(float l){
        this.lon = l;
    }

    public float getLat() {
        return lat;
    }

    public float getLon() {
        return lon;
    }
}
